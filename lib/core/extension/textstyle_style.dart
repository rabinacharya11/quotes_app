
import 'package:flutter/material.dart';




extension on TextStyle {

  TextStyle get montserrat {
    return copyWith(
      fontFamily: 'Montserrat',
    );
  }

  TextStyle get poppins {
    return copyWith(
      fontFamily: 'Poppins',
    );
  }

  TextStyle get ingridDarling {
    return copyWith(
      fontFamily: 'Ingrid Darling',
    );
  }
}